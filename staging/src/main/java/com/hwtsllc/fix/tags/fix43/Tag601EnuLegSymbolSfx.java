package com.hwtsllc.fix.tags.fix43;

import com.hwtsllc.fix.datatypes.FIX43;
import com.hwtsllc.fix.datatypes.FIX43Abstract;
import com.hwtsllc.fix.datatypes.MyEnumSymbolSfx;
import com.hwtsllc.fix.interfaces.LogDataString;
import com.hwtsllc.fix.interfaces.LogValuePairString;
import com.hwtsllc.fix.interfaces.LogVerboseString;

/**
 *  65 (same as 65, 312. 601,)
 *  SymbolSfx
 *  String
 *  <p>
 *  Additional information about the security
 *  <p>
 *  (e.g. preferred, warrants, etc.).
 *  <p>
 *  Note also see SecurityType (167).
 *  <p>
 *  As defined in the NYSE Stock and bond Symbol Directory and in the AMEX Fitch Directory.
 *  <p></p>
 *  312
 *  UnderlyingSymbolSfx
 *  String
 *  <p>
 *  Underlying security’s SymbolSfx.
 *  <p>
 *  See SymbolSfx (65) field for description
 *  <p></p>
 *  601
 *  LegSymbolSfx
 *  String
 *  <p>
 *  Multileg instrument's individual  security’s SymbolSfx.
 *  <p>
 *  See SymbolSfx (65) field for description
 *  <p></p>
 *  Valid values:
 *  For Fixed Income
 *  <p>    CD - EUCP with lump-sum interest rather than discount price
 *  <p>    WI - When-Issued for a security to be reissued under an old CUSIP or ISIN
 */
public class Tag601EnuLegSymbolSfx extends FIX43Abstract implements LogValuePairString, LogVerboseString, LogDataString {
    private final MyEnumSymbolSfx dataValue;

    public final static MyEnumSymbolSfx TESTA_ENU_LEG_SYMBOL_SFX
            = MyEnumSymbolSfx.WHEN_ISSUED;
    public final static MyEnumSymbolSfx TESTB_ENU_LEG_SYMBOL_SFX
            = MyEnumSymbolSfx.EUCP;

    public Tag601EnuLegSymbolSfx(MyEnumSymbolSfx dataValue) {
        setFixType(FIX43.FIX601_ENU_LEG_SYMBOL_SFX);
        this.dataValue = dataValue;
    }

    /**
     * standard wrapper to retrieve the build a standard fix message for this tag
     */
    @Override
    public String toValuePairString() {
        return toFIXIDString()
                .concat("=")
                .concat(dataValue.toString());
    }
    /**
     * standard wrapper to format a detailed string describing this data field
     */
    @Override
    public String toVerboseString() {
        return super.toVerboseString()
                .concat("\n\tDataValue[")
                .concat(toString())
                .concat("]")
                .concat("\n\tValuePair[")
                .concat(toValuePairString())
                .concat("]")
                .concat("\n\tDataID[")
                .concat(toDataIDString())
                .concat("]")
                .concat("\n\tDataName[")
                .concat(toDataNameString())
                .concat("]")
                .concat("\n\tDataDescription[")
                .concat(toDataDescriptionString())
                .concat("]")
                ;
    }
    /**
     * wrapper to return the ID of the underlying Data
     */
    @Override
    public String toDataIDString() {
        return dataValue.toFIXIDString();
    }
    /**
     * wrapper to return the Name of the underlying Data
     */
    @Override
    public String toDataNameString() {
        return dataValue.toFIXNameString();
    }
    /**
     * wrapper to return the Description of the underlying Data
     */
    @Override
    public String toDataDescriptionString() {
        return dataValue.toFIXDescriptionString();
    }
    /**
     * standard wrapper to format a simple string describing the data
     */
    @Override
    public String toString() {
        return dataValue.toString();
    }

    /**
     *
     * @param args   no args used at this time
     */
    public static void main(String[] args) {
        Tag601EnuLegSymbolSfx tagData;

        tagData = new Tag601EnuLegSymbolSfx(TESTA_ENU_LEG_SYMBOL_SFX);
        System.out.println(tagData.toVerboseString());

        tagData = new Tag601EnuLegSymbolSfx(TESTB_ENU_LEG_SYMBOL_SFX);
        System.out.println(tagData.toVerboseString());

        // loop around the ENUM and process
        for ( MyEnumSymbolSfx oneEnum : MyEnumSymbolSfx.values()) {
            System.out.println( new Tag601EnuLegSymbolSfx(oneEnum).toVerboseString() );
        }
    }
}
