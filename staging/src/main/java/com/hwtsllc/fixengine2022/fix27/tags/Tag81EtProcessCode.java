package com.hwtsllc.fixengine2022.fix27.tags;

import com.hwtsllc.fixengine2022.datatypes.FIXType;
import com.hwtsllc.fixengine2022.datatypes.TagTypeAbstract;
import com.hwtsllc.fixengine2022.fix27.enums.Enum81ProcessCode;

public class Tag81EtProcessCode extends TagTypeAbstract {
    private final Enum81ProcessCode dataValue;

    public final static Enum81ProcessCode TESTA_ET_PROCESS_CODE = Enum81ProcessCode.REGULAR;
    public final static Enum81ProcessCode TESTB_ET_PROCESS_CODE = Enum81ProcessCode.PLAN_SPONSOR;

    public Tag81EtProcessCode(Enum81ProcessCode dataValue) {
        setFixType(FIXType.FIX81_ET_PROCESS_CODE);
        setDataValue(dataValue.getID());
        this.dataValue = dataValue;
    }

    /**
     *
     * @param args      Not used.
     */
    public static void main(String[] args) {
        Tag81EtProcessCode tagData = new Tag81EtProcessCode(TESTA_ET_PROCESS_CODE);
        System.out.println(tagData);
        System.out.println(tagData.toLogStringVerbose());
        System.out.println(tagData.toFixTagValuePairString());
    }
}