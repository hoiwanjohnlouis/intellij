package com.hwtsllc.fixengine2022.fix27.tags;

import com.hwtsllc.fixengine2022.datatypes.FIXType;
import com.hwtsllc.fixengine2022.datatypes.TagTypeAbstract;
import com.hwtsllc.fixengine2022.fix27.enums.Enum39OrdStatus;

public class Tag39OrdStatus extends TagTypeAbstract {
    private final Enum39OrdStatus dataValue;

    public final static Enum39OrdStatus TESTA_ORDER_STATUS = Enum39OrdStatus.NEW; // fake data
    public final static Enum39OrdStatus TESTB_ORDER_STATUS = Enum39OrdStatus.ACCEPTED_FOR_BIDDING;

    public Tag39OrdStatus(Enum39OrdStatus dataValue) {
        setFixType(FIXType.FIX39_ORD_STATUS);
        setDataValue(dataValue.getID());
        this.dataValue = dataValue;
    }

    /**
     *
     * @param args      Not used.
     */
    public static void main(String[] args) {
        Tag39OrdStatus tagData = new Tag39OrdStatus(TESTA_ORDER_STATUS);
        System.out.println(tagData);
        System.out.println(tagData.toLogStringVerbose());
        System.out.println(tagData.toFixTagValuePairString());
    }
}
