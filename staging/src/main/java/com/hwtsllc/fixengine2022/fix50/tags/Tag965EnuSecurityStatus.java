package com.hwtsllc.fixengine2022.fix50.tags;

import com.hwtsllc.fixengine2022.datatypes.FIX50;
import com.hwtsllc.fixengine2022.datatypes.FIX50Abstract;
import com.hwtsllc.fixengine2022.fix50.enums.Enum965SecurityStatus;
import com.hwtsllc.fixengine2022.interfaces.FixTagValuePairString;
import com.hwtsllc.fixengine2022.interfaces.LogStringVerbose;

/**
 *  965
 *  SecurityStatus
 *  String
 *  <p>
 *  Used for derivatives.
 *  <p>
 *  Denotes the current state of the Instrument.
 *  <p></p>
 *  Valid values:
 *  <p>    1 - Active
 *  <p>    2 - Inactive
 */
public class Tag965EnuSecurityStatus extends FIX50Abstract implements FixTagValuePairString, LogStringVerbose {
    private final Enum965SecurityStatus dataValue;

    public final static Enum965SecurityStatus TESTA_ENU_SECURITY_STATUS
            = Enum965SecurityStatus.ACTIVE;
    public final static Enum965SecurityStatus TESTB_ENU_SECURITY_STATUS
            = Enum965SecurityStatus.INACTIVE;

    public Tag965EnuSecurityStatus(Enum965SecurityStatus dataValue) {
        setFixType(FIX50.FIX965_ENU_SECURITY_STATUS);
        this.dataValue = dataValue;
    }

    public String getDataValue() {
        return this.dataValue.getID();
    }
    /**
     * standard wrapper to retrieve the build a standard fix message for this tag
     */
    @Override
    public String toFixTagValuePairString() {
        return getID()
                .concat("=")
                .concat(getDataValue());
    }
    /**
     * standard wrapper to format a detailed string describing this data field
     */
    @Override
    public String toLogStringVerbose() {
        return super.toLogStringVerbose()
                .concat("\n\tDataValue[")
                .concat(toString())
                .concat("]");
    }
    /**
     * standard wrapper to format a simple string describing the data
     */
    @Override
    public String toString() {
        return String.valueOf(getDataValue());
    }

    /**
     *
     * @param args   no args used at this time
     */
    public static void main(String[] args) {
        Tag965EnuSecurityStatus tagData;

        tagData = new Tag965EnuSecurityStatus(TESTA_ENU_SECURITY_STATUS);
        System.out.println(tagData);
        System.out.println(tagData.toLogStringVerbose());
        System.out.println(tagData.toFixTagValuePairString());

        tagData = new Tag965EnuSecurityStatus(TESTB_ENU_SECURITY_STATUS);
        System.out.println(tagData);
        System.out.println(tagData.toLogStringVerbose());
        System.out.println(tagData.toFixTagValuePairString());
    }
}
