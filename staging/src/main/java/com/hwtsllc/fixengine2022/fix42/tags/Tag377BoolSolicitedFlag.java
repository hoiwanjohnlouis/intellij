package com.hwtsllc.fixengine2022.fix42.tags;

import com.hwtsllc.fixengine2022.datatypes.FIX42;
import com.hwtsllc.fixengine2022.datatypes.FIX42Abstract;
import com.hwtsllc.fixengine2022.datatypes.MyBooleanType;
import com.hwtsllc.fixengine2022.interfaces.LogValuePairString;
import com.hwtsllc.fixengine2022.interfaces.LogVerboseString;

public class Tag377BoolSolicitedFlag extends FIX42Abstract implements LogValuePairString, LogVerboseString {
    private final MyBooleanType dataValue;

    public final static MyBooleanType TESTA_BOOL_SOLICITED_FLAG = MyBooleanType.NO ;     // N - Was not solicited
    public final static MyBooleanType TESTB_BOOL_SOLICITED_FLAG = MyBooleanType.YES;     // Y - Was solicited

    public Tag377BoolSolicitedFlag(MyBooleanType dataValue) {
        setFixType(FIX42.FIX377_BOOL_SOLICITED_FLAG);
        this.dataValue = dataValue;
    }

    public String getDataValue() {
        return this.dataValue.getID();
    }
    /**
     * standard wrapper to retrieve the build a standard fix message for this tag
     */
    @Override
    public String toValuePairString() {
        return getID()
                .concat("=")
                .concat(getDataValue());
    }
    /**
     * standard wrapper to format a detailed string describing this data field
     */
    @Override
    public String toVerboseString() {
        return super.toVerboseString()
                .concat("\n\tDataValue[")
                .concat(toString())
                .concat("]");
    }
    /**
     * standard wrapper to format a simple string describing the data
     */
    @Override
    public String toString() {
        return String.valueOf(getDataValue());
    }

    /**
     *
     * @param args   no args used at this time
     */
    public static void main(String[] args) {
        Tag377BoolSolicitedFlag tagData;

        tagData = new Tag377BoolSolicitedFlag(TESTA_BOOL_SOLICITED_FLAG);
        System.out.println(tagData);
        System.out.println(tagData.toVerboseString());
        System.out.println(tagData.toValuePairString());

        tagData = new Tag377BoolSolicitedFlag(TESTB_BOOL_SOLICITED_FLAG);
        System.out.println(tagData);
        System.out.println(tagData.toVerboseString());
        System.out.println(tagData.toValuePairString());
    }
}
