package com.hwtsllc.fixengine2022.fix42.tags;

import com.hwtsllc.fixengine2022.datatypes.FIX42;
import com.hwtsllc.fixengine2022.datatypes.FIX42Abstract;
import com.hwtsllc.fixengine2022.fix42.enums.Enum298QuoteCancelType;
import com.hwtsllc.fixengine2022.interfaces.LogDataString;
import com.hwtsllc.fixengine2022.interfaces.LogValuePairString;
import com.hwtsllc.fixengine2022.interfaces.LogVerboseString;

/**
 *  298
 *  QuoteCancelType
 *  int
 *  <p>
 *  Identifies the type of  quote cancel.
 *  <p></p>
 *  Valid values:
 *  <p>    1-5 msg types
 *  <p>    "1 - Cancel for Symbol(s)"
 *  <p>    "2 - Cancel for Security Type(s)"
 *  <p>    "3 - Cancel for Underlying Symbol"
 *  <p>    "4 - Cancel All Quotes"
 *  <p>    "5 - Cancel quote specified in QuoteID"
 */
public class Tag298EnuQuoteCancelType extends FIX42Abstract implements LogValuePairString, LogVerboseString, LogDataString {
    private final Enum298QuoteCancelType dataValue;

    public final static Enum298QuoteCancelType TESTA_ENU_QUOTE_CANCEL_TYPE
            = Enum298QuoteCancelType.CANCEL_FOR_SYMBOL;
    public final static Enum298QuoteCancelType TESTB_ENU_QUOTE_CANCEL_TYPE
            = Enum298QuoteCancelType.CANCEL_ALL_QUOTES;

    public Tag298EnuQuoteCancelType(Enum298QuoteCancelType dataValue) {
        setFixType(FIX42.FIX298_ENU_QUOTE_CANCEL_TYPE);
        this.dataValue = dataValue;
    }

    /**
     * standard wrapper to retrieve the build a standard fix message for this tag
     */
    @Override
    public String toValuePairString() {
        return toFIXIDString()
                .concat("=")
                .concat(dataValue.toString());
    }
    /**
     * standard wrapper to format a detailed string describing this data field
     */
    @Override
    public String toVerboseString() {
        return super.toVerboseString()
                .concat("\n\tDataValue[")
                .concat(toString())
                .concat("]")
                .concat("\n\tValuePair[")
                .concat(toValuePairString())
                .concat("]")
                .concat("\n\tDataID[")
                .concat(toDataIDString())
                .concat("]")
                .concat("\n\tDataName[")
                .concat(toDataNameString())
                .concat("]")
                .concat("\n\tDataDescription[")
                .concat(toDataDescriptionString())
                .concat("]")
                ;
    }
    /**
     * wrapper to return the ID of the underlying Data
     */
    @Override
    public String toDataIDString() {
        return dataValue.toFIXIDString();
    }
    /**
     * wrapper to return the Name of the underlying Data
     */
    @Override
    public String toDataNameString() {
        return dataValue.toFIXNameString();
    }
    /**
     * wrapper to return the Description of the underlying Data
     */
    @Override
    public String toDataDescriptionString() {
        return dataValue.toFIXDescriptionString();
    }
    /**
     * standard wrapper to format a simple string describing the data
     */
    @Override
    public String toString() {
        return dataValue.toString();
    }

    /**
     *
     * @param args   no args used at this time
     */
    public static void main(String[] args) {
        Tag298EnuQuoteCancelType tagData;

        tagData = new Tag298EnuQuoteCancelType(TESTA_ENU_QUOTE_CANCEL_TYPE);
        System.out.println(tagData.toVerboseString());

        tagData = new Tag298EnuQuoteCancelType(TESTB_ENU_QUOTE_CANCEL_TYPE);
        System.out.println(tagData.toVerboseString());

        // loop around the ENUM and display
        for (Enum298QuoteCancelType oneEnum : Enum298QuoteCancelType.values()) {
            System.out.println( new Tag298EnuQuoteCancelType(oneEnum).toVerboseString() );
        }
    }
}
