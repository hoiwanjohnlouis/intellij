package com.hwtsllc.fixengine2022.fix41.tags;

import com.hwtsllc.fixengine2022.datatypes.FIX41;
import com.hwtsllc.fixengine2022.datatypes.FIX41Abstract;
import com.hwtsllc.fixengine2022.fix41.enums.Enum160SettlInstMode;
import com.hwtsllc.fixengine2022.interfaces.FixTagValuePairString;
import com.hwtsllc.fixengine2022.interfaces.LogStringVerbose;

public class Tag160EnuSettlInstMode extends FIX41Abstract implements FixTagValuePairString, LogStringVerbose {
    private final Enum160SettlInstMode dataValue;

    public final static Enum160SettlInstMode TESTA_ENU_SETTL_INST_MODE = Enum160SettlInstMode.DEFAULT;
    public final static Enum160SettlInstMode TESTB_ENU_SETTL_INST_MODE = Enum160SettlInstMode.REQUEST_REJECT;

    public Tag160EnuSettlInstMode(Enum160SettlInstMode dataValue) {
        setFixType(FIX41.FIX160_ENU_SETTL_INST_MODE);
        this.dataValue = dataValue;
    }

    public String getDataValue() {
        return this.dataValue.getID();
    }
    /**
     * standard wrapper to retrieve the build a standard fix message for this tag
     */
    @Override
    public String toFixTagValuePairString() {
        return getID()
                .concat("=")
                .concat(getDataValue());
    }
    /**
     * standard wrapper to format a detailed string describing this data field
     */
    @Override
    public String toLogStringVerbose() {
        return super.toLogStringVerbose()
                .concat("\n\tDataValue[")
                .concat(toString())
                .concat("]");
    }
    /**
     * standard wrapper to format a simple string describing the data
     */
    @Override
    public String toString() {
        return String.valueOf(getDataValue());
    }

    /**
     *
     * @param args   no args used at this time
     */
    public static void main(String[] args) {
        Tag160EnuSettlInstMode tagData;

        tagData = new Tag160EnuSettlInstMode(TESTA_ENU_SETTL_INST_MODE);
        System.out.println(tagData);
        System.out.println(tagData.toLogStringVerbose());
        System.out.println(tagData.toFixTagValuePairString());

        tagData = new Tag160EnuSettlInstMode(TESTB_ENU_SETTL_INST_MODE);
        System.out.println(tagData);
        System.out.println(tagData.toLogStringVerbose());
        System.out.println(tagData.toFixTagValuePairString());
    }
}
