package com.hwtsllc.fixengine2022.fix41.tags;

import com.hwtsllc.fixengine2022.datatypes.FIX41;
import com.hwtsllc.fixengine2022.datatypes.FIX41Abstract;
import com.hwtsllc.fixengine2022.fix41.enums.Enum169StandInstDbType;
import com.hwtsllc.fixengine2022.interfaces.FixTagValuePairString;
import com.hwtsllc.fixengine2022.interfaces.LogStringVerbose;

public class Tag169EnuStandInstDBType extends FIX41Abstract implements FixTagValuePairString, LogStringVerbose {
    private final Enum169StandInstDbType dataValue;

    public final static Enum169StandInstDbType TESTA_ENU_STAND_INST_DB_TYPE = Enum169StandInstDbType.DTC_SID;
    public final static Enum169StandInstDbType TESTB_ENU_STAND_INST_DB_TYPE = Enum169StandInstDbType.ACCOUNT_NET;

    public Tag169EnuStandInstDBType(Enum169StandInstDbType dataValue) {
        setFixType(FIX41.FIX169_STAND_INST_DB_TYPE);
        this.dataValue = dataValue;
    }

    public String getDataValue() {
        return this.dataValue.getID();
    }
    /**
     * standard wrapper to retrieve the build a standard fix message for this tag
     */
    @Override
    public String toFixTagValuePairString() {
        return getID()
                .concat("=")
                .concat(getDataValue());
    }
    /**
     * standard wrapper to format a detailed string describing this data field
     */
    @Override
    public String toLogStringVerbose() {
        return super.toLogStringVerbose()
                .concat("\n\tDataValue[")
                .concat(toString())
                .concat("]");
    }
    /**
     * standard wrapper to format a simple string describing the data
     */
    @Override
    public String toString() {
        return String.valueOf(getDataValue());
    }

    /**
     *
     * @param args   no args used at this time
     */
    public static void main(String[] args) {
        Tag169EnuStandInstDBType tagData;

        tagData = new Tag169EnuStandInstDBType(TESTA_ENU_STAND_INST_DB_TYPE);
        System.out.println(tagData);
        System.out.println(tagData.toLogStringVerbose());
        System.out.println(tagData.toFixTagValuePairString());

        tagData = new Tag169EnuStandInstDBType(TESTB_ENU_STAND_INST_DB_TYPE);
        System.out.println(tagData);
        System.out.println(tagData.toLogStringVerbose());
        System.out.println(tagData.toFixTagValuePairString());
    }
}
