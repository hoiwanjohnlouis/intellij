package com.hwtsllc.fixengine2022.datatypes;

import com.hwtsllc.fixengine2022.interfaces.LogFIXString;
import com.hwtsllc.fixengine2022.interfaces.LogVerboseString;

/**
 *  996 (same as 996, 998, 999,)
 *  UnitofMeasure
 *  String
 *  <p>
 *  Physical unit of measure for Derivative products.
 *  <p>
 *  NOTE: Additional values may be used by mutual agreement of the counterparties
 *  <p>
 *  (http://www.unc.edu/~rowlett/units/index.html is a good source for units)
 *  <p></p>
 *  998
 *  UnderlyingUnitofMeasure
 *  String
 *  <p>
 *  Same as UnitofMeasure (996).
 *  <p></p>
 *  999
 *  LegUnitofMeasure
 *  String
 *  <p>
 *  Same as UnitofMeasure (996).
 *  <p></p>
 *  Valid values:
 *  <p>    Bbl - Barrels
 *  <p>    Bcf - Billion cubic feet
 *  <p>    Bu - Bushels
 *  <p>    lbs - pounds
 *  <p>    Gal - Gallons
 *  <p></p>
 *  <p>    MMbbl - Million Barrels
 *  <p>    MMBtu - One Million BTU
 *  <p>    MWh - Megawatt hours
 *  <p>    oz_tr - Troy Ounces
 *  <p>    t - Metric Tons (aka Tonne)
 *  <p></p>
 *  <p>    tn - Tons (US)
 *  <p>    USD - US Dollars
 */
public enum MyEnumUnitOfMeasure implements LogFIXString, LogVerboseString {
    BARRELS( "Bbl", "BARRELS", "Bbl - Barrels" ),
    BILLION_CUBIC_FEET( "Bcf", "BILLION_CUBIC_FEET", "Bcf - Billion cubic feet" ),
    BUSHELS( "Bu", "BUSHELS", "Bu - Bushels" ),
    POUNDS( "lbs", "POUNDS", "lbs - pounds" ),
    GALLONS( "Gal", "GALLONS", "Gal - Gallons" ),

    MILLION_BARRELS( "MMbbl", "MILLION_BARRELS", "MMbbl - Million Barrels" ),
    MILLION_BTU( "MMBtu", "MILLION_BTU", "MMBtu - One Million BTU" ),
    MEGAWATT_HOURS( "MWh", "MEGAWATT_HOURS", "MWh - Megawatt hours" ),
    TROY_OUNCES( "oz_tr", "TROY_OUNCES", "oz_tr - Troy Ounces" ),
    METRIC_TONS( "t", "METRIC_TONS", "t - Metric Tons (aka Tonne)" ),

    US_TONS( "tn", "US_TONS", "tn - Tons (US)" ),
    US_DOLLARS( "USD", "US_DOLLARS", "USD - US Dollars" ),
    ;

    private final String id;
    private final String name;
    private final String description;

    MyEnumUnitOfMeasure(final String id, final String name, final String description) {
        this.id = id;
        this.name = name;
        this.description = description;
    }

    /**
     * standard wrapper to retrieve the specific enum name
     */
    @Override
    public String toEnumLabelString() {
        return this.name();
    }
    /**
     * standard wrapper to retrieve the specific fix action code for this enum. eg: the first field
     */
    @Override
    public String toEnumIDString() {
        return id;
    }
    /**
     * standard wrapper to retrieve the specific fix name for this enum. eg: the second field
     */
    @Override
    public String toEnumNameString() {
        return name;
    }
    /**
     * standard wrapper to retrieve the specific fix description for this enum. eg: the third field
     */
    @Override
    public String toEnumDescriptionString() {
        return description;
    }
    /**
     * standard wrapper to format a detailed string describing this enum
     */
    @Override
    public String toVerboseString() {
        return this.getClass().getSimpleName()
                .concat("\n\tEnumName[")
                .concat( toEnumLabelString())
                .concat("]")
                .concat("\n\tAction[")
                .concat( toEnumIDString())
                .concat("]")
                .concat("\n\tName[")
                .concat( toEnumNameString())
                .concat("]")
                .concat("\n\tDescription[")
                .concat( toEnumDescriptionString())
                .concat("]");
    }
    /**
     * standard wrapper to format a simple string describing this enum
     */
    @Override
    public String toString() {
        return toEnumLabelString()
                .concat("=[")
                .concat( toEnumIDString())
                .concat(",")
                .concat( toEnumNameString())
                .concat(",")
                .concat( toEnumDescriptionString())
                .concat("]");
    }

    /**
     *
     */
    public static void main(String[] args) {
        /*
         * dump all the enum values
         */
        for (MyEnumUnitOfMeasure oneEnum : MyEnumUnitOfMeasure.values()) {
            System.out.println(oneEnum);
        }
    }
}
