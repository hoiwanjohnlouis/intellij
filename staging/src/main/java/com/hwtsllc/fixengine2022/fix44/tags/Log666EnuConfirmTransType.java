package com.hwtsllc.fixengine2022.fix44.tags;

import com.hwtsllc.fixengine2022.datatypes.FIX44;
import com.hwtsllc.fixengine2022.datatypes.FIX44Abstract;
import com.hwtsllc.fixengine2022.fix44.enums.Enum666ConfirmTransType;
import com.hwtsllc.fixengine2022.interfaces.LogValuePairString;
import com.hwtsllc.fixengine2022.interfaces.LogVerboseString;

/**
 *  666
 *  ConfirmTransType
 *  Identifies the Confirmation transaction type.
 *  Valid values:
 *      0 - New
 *      1 - Replace
 *      2 - Cancel
 */
public class Log666EnuConfirmTransType extends FIX44Abstract implements LogValuePairString, LogVerboseString {
    private final Enum666ConfirmTransType dataValue;

    public final static Enum666ConfirmTransType TESTA_ENU_CONFIRM_TRANS_TYPE
            = Enum666ConfirmTransType.CANCEL;
    public final static Enum666ConfirmTransType TESTB_ENU_CONFIRM_TRANS_TYPE
            = Enum666ConfirmTransType.NEW;

    public Log666EnuConfirmTransType(Enum666ConfirmTransType dataValue) {
        setFixType(FIX44.FIX666_ENU_CONFIRM_TRANS_TYPE);
        this.dataValue = dataValue;
    }

    public String getDataValue() {
        return this.dataValue.getID();
    }
    /**
     * standard wrapper to retrieve the build a standard fix message for this tag
     */
    @Override
    public String toValuePairString() {
        return getID()
                .concat("=")
                .concat(getDataValue());
    }
    /**
     * standard wrapper to format a detailed string describing this data field
     */
    @Override
    public String toVerboseString() {
        return super.toVerboseString()
                .concat("\n\tDataValue[")
                .concat(toString())
                .concat("]");
    }
    /**
     * standard wrapper to format a simple string describing the data
     */
    @Override
    public String toString() {
        return String.valueOf(getDataValue());
    }

    /**
     *
     * @param args   no args used at this time
     */
    public static void main(String[] args) {
        Log666EnuConfirmTransType tagData;

        tagData = new Log666EnuConfirmTransType(TESTA_ENU_CONFIRM_TRANS_TYPE);
        System.out.println(tagData);
        System.out.println(tagData.toVerboseString());
        System.out.println(tagData.toValuePairString());

        tagData = new Log666EnuConfirmTransType(TESTB_ENU_CONFIRM_TRANS_TYPE);
        System.out.println(tagData);
        System.out.println(tagData.toVerboseString());
        System.out.println(tagData.toValuePairString());
    }
}
