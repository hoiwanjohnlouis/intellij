package com.hwtsllc.fixengine2022.fix43.tags;

import com.hwtsllc.fixengine2022.datatypes.FIX43;
import com.hwtsllc.fixengine2022.datatypes.FIX43Abstract;
import com.hwtsllc.fixengine2022.fix43.enums.Enum452PartyRole;
import com.hwtsllc.fixengine2022.interfaces.FixTagValuePairString;
import com.hwtsllc.fixengine2022.interfaces.LogStringVerbose;

public class Tag452EnuPartyRole extends FIX43Abstract implements FixTagValuePairString, LogStringVerbose {
    private final Enum452PartyRole dataValue;

    public final static Enum452PartyRole TESTA_ENU_PARTY_ROLE = Enum452PartyRole.ACCEPTABLE_COUNTERPARTY;
    public final static Enum452PartyRole TESTB_ENU_PARTY_ROLE = Enum452PartyRole.CONTRA_CLEARING_FIRM;

    public Tag452EnuPartyRole(Enum452PartyRole dataValue) {
        setFixType(FIX43.FIX452_ENU_PARTY_ROLE);
        this.dataValue = dataValue;
    }

    public String getDataValue() {
        return this.dataValue.getID();
    }
    /**
     * standard wrapper to retrieve the build a standard fix message for this tag
     */
    @Override
    public String toFixTagValuePairString() {
        return getID()
                .concat("=")
                .concat(getDataValue());
    }
    /**
     * standard wrapper to format a detailed string describing this data field
     */
    @Override
    public String toLogStringVerbose() {
        return super.toLogStringVerbose()
                .concat("\n\tDataValue[")
                .concat(toString())
                .concat("]");
    }
    /**
     * standard wrapper to format a simple string describing the data
     */
    @Override
    public String toString() {
        return String.valueOf(getDataValue());
    }

    /**
     *
     * @param args   no args used at this time
     */
    public static void main(String[] args) {
        Tag452EnuPartyRole tagData;

        tagData = new Tag452EnuPartyRole(TESTA_ENU_PARTY_ROLE);
        System.out.println(tagData);
        System.out.println(tagData.toLogStringVerbose());
        System.out.println(tagData.toFixTagValuePairString());

        tagData = new Tag452EnuPartyRole(TESTB_ENU_PARTY_ROLE);
        System.out.println(tagData);
        System.out.println(tagData.toLogStringVerbose());
        System.out.println(tagData.toFixTagValuePairString());
    }
}
