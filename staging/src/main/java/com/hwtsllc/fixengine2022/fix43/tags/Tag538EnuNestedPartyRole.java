package com.hwtsllc.fixengine2022.fix43.tags;

import com.hwtsllc.fixengine2022.datatypes.FIX43;
import com.hwtsllc.fixengine2022.datatypes.FIX43Abstract;
import com.hwtsllc.fixengine2022.fix43.enums.Enum538NestedPartyRole;
import com.hwtsllc.fixengine2022.interfaces.FixTagValuePairString;
import com.hwtsllc.fixengine2022.interfaces.LogStringVerbose;

public class Tag538EnuNestedPartyRole extends FIX43Abstract implements FixTagValuePairString, LogStringVerbose {
    private final Enum538NestedPartyRole dataValue;

    public final static Enum538NestedPartyRole TESTA_ENU_NESTED_PARTY_ROLE
            = Enum538NestedPartyRole.INTERESTED_PARTY;
    public final static Enum538NestedPartyRole TESTB_ENU_NESTED_PARTY_ROLE
            = Enum538NestedPartyRole.ALLOCATION_ENTITY;

    public Tag538EnuNestedPartyRole(Enum538NestedPartyRole dataValue) {
        setFixType(FIX43.FIX538_ENU_NESTED_PARTY_ROLE);
        this.dataValue = dataValue;
    }

    public String getDataValue() {
        return this.dataValue.getID();
    }
    /**
     * standard wrapper to retrieve the build a standard fix message for this tag
     */
    @Override
    public String toFixTagValuePairString() {
        return getID()
                .concat("=")
                .concat(getDataValue());
    }
    /**
     * standard wrapper to format a detailed string describing this data field
     */
    @Override
    public String toLogStringVerbose() {
        return super.toLogStringVerbose()
                .concat("\n\tDataValue[")
                .concat(toString())
                .concat("]");
    }
    /**
     * standard wrapper to format a simple string describing the data
     */
    @Override
    public String toString() {
        return String.valueOf(getDataValue());
    }

    /**
     *
     * @param args   no args used at this time
     */
    public static void main(String[] args) {
        Tag538EnuNestedPartyRole tagData;

        tagData = new Tag538EnuNestedPartyRole(TESTA_ENU_NESTED_PARTY_ROLE);
        System.out.println(tagData);
        System.out.println(tagData.toLogStringVerbose());
        System.out.println(tagData.toFixTagValuePairString());

        tagData = new Tag538EnuNestedPartyRole(TESTB_ENU_NESTED_PARTY_ROLE);
        System.out.println(tagData);
        System.out.println(tagData.toLogStringVerbose());
        System.out.println(tagData.toFixTagValuePairString());
    }
}
