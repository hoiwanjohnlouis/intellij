package com.hwtsllc.fixengine2022.fix43.tags;

import com.hwtsllc.fixengine2022.datatypes.FIX43;
import com.hwtsllc.fixengine2022.datatypes.FIX43Abstract;
import com.hwtsllc.fixengine2022.fix43.enums.Enum591PreallocMethod;
import com.hwtsllc.fixengine2022.interfaces.LogValuePairString;
import com.hwtsllc.fixengine2022.interfaces.LogVerboseString;

public class Log591EnuPreallocMethod extends FIX43Abstract implements LogValuePairString, LogVerboseString {
    private final Enum591PreallocMethod dataValue;

    public final static Enum591PreallocMethod TESTA_ENU_PREALLOC_METHOD
            = Enum591PreallocMethod.DO_NOT_PRO_RATA;
    public final static Enum591PreallocMethod TESTB_ENU_PREALLOC_METHOD
            = Enum591PreallocMethod.PRO_RATA;

    public Log591EnuPreallocMethod(Enum591PreallocMethod dataValue) {
        setFixType(FIX43.FIX591_ENU_PREALLOC_METHOD);
        this.dataValue = dataValue;
    }

    public String getDataValue() {
        return this.dataValue.getID();
    }
    /**
     * standard wrapper to retrieve the build a standard fix message for this tag
     */
    @Override
    public String toValuePairString() {
        return getID()
                .concat("=")
                .concat(getDataValue());
    }
    /**
     * standard wrapper to format a detailed string describing this data field
     */
    @Override
    public String toVerboseString() {
        return super.toVerboseString()
                .concat("\n\tDataValue[")
                .concat(toString())
                .concat("]");
    }
    /**
     * standard wrapper to format a simple string describing the data
     */
    @Override
    public String toString() {
        return String.valueOf(getDataValue());
    }

    /**
     *
     * @param args   no args used at this time
     */
    public static void main(String[] args) {
        Log591EnuPreallocMethod tagData;

        tagData = new Log591EnuPreallocMethod(TESTA_ENU_PREALLOC_METHOD);
        System.out.println(tagData);
        System.out.println(tagData.toVerboseString());
        System.out.println(tagData.toValuePairString());

        tagData = new Log591EnuPreallocMethod(TESTB_ENU_PREALLOC_METHOD);
        System.out.println(tagData);
        System.out.println(tagData.toVerboseString());
        System.out.println(tagData.toValuePairString());
    }
}
