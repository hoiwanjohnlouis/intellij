package com.hwtsllc.fixengine.tags.fix44;

import com.hwtsllc.fixengine.datatypes.FIX44;
import com.hwtsllc.fixengine.datatypes.FIX44Abstract;
import com.hwtsllc.fixengine.datatypes.MyEnumDiscretionOrPegScope;
import com.hwtsllc.fixengine.interfaces.LogDataString;
import com.hwtsllc.fixengine.interfaces.LogValuePairString;
import com.hwtsllc.fixengine.interfaces.LogVerboseString;

/**
 *  840 (same as 840, 846,)
 *  PegScope
 *  int
 *  <p>
 *  The scope of the peg
 *  <p></p>
 *  846
 *  DiscretionScope
 *  int
 *  <p>
 *  The scope of the discretion
 *  <p></p>
 *  Valid values:
 *  <p>    1 - Local (Exchange, ECN, ATS)
 *  <p>    2 - National
 *  <p>    3 - Global
 *  <p>    4 - National excluding local
 */
public class Tag846EnuDiscretionScope extends FIX44Abstract implements LogValuePairString, LogVerboseString, LogDataString {
    private final MyEnumDiscretionOrPegScope dataValue;

    public final static MyEnumDiscretionOrPegScope TESTA_ENU_DISCRETION_SCOPE
            = MyEnumDiscretionOrPegScope.NATIONAL;
    public final static MyEnumDiscretionOrPegScope TESTB_ENU_DISCRETION_SCOPE
            = MyEnumDiscretionOrPegScope.NATIONAL_EXCLUDING_LOCAL;

    public Tag846EnuDiscretionScope( MyEnumDiscretionOrPegScope dataValue) {
        setFixType(FIX44.FIX846_ENU_DISCRETION_SCOPE);
        this.dataValue = dataValue;
    }

    /**
     * standard wrapper to retrieve the build a standard fix message for this tag
     */
    @Override
    public String toValuePairString() {
        return toFIXIDString()
                .concat("=")
                .concat(dataValue.toString());
    }
    /**
     * standard wrapper to format a detailed string describing this data field
     */
    @Override
    public String toVerboseString() {
        return super.toVerboseString()
                .concat("\n\tDataValue[")
                .concat(toString())
                .concat("]")
                .concat("\n\tValuePair[")
                .concat(toValuePairString())
                .concat("]")
                .concat("\n\tDataID[")
                .concat(toDataIDString())
                .concat("]")
                .concat("\n\tDataName[")
                .concat(toDataNameString())
                .concat("]")
                .concat("\n\tDataDescription[")
                .concat(toDataDescriptionString())
                .concat("]")
                ;
    }
    /**
     * wrapper to return the ID of the underlying Data
     */
    @Override
    public String toDataIDString() {
        return dataValue.toFIXIDString();
    }
    /**
     * wrapper to return the Name of the underlying Data
     */
    @Override
    public String toDataNameString() {
        return dataValue.toFIXNameString();
    }
    /**
     * wrapper to return the Description of the underlying Data
     */
    @Override
    public String toDataDescriptionString() {
        return dataValue.toFIXDescriptionString();
    }
    /**
     * standard wrapper to format a simple string describing the data
     */
    @Override
    public String toString() {
        return dataValue.toString();
    }

    /**
     *
     * @param args   no args used at this time
     */
    public static void main(String[] args) {
        Tag846EnuDiscretionScope tagData;

        tagData = new Tag846EnuDiscretionScope(TESTA_ENU_DISCRETION_SCOPE);
        System.out.println(tagData.toVerboseString());

        tagData = new Tag846EnuDiscretionScope(TESTB_ENU_DISCRETION_SCOPE);
        System.out.println(tagData.toVerboseString());

        // loop around the ENUM and process
        for ( MyEnumDiscretionOrPegScope oneEnum : MyEnumDiscretionOrPegScope.values()) {
            System.out.println( new Tag846EnuDiscretionScope(oneEnum).toVerboseString() );
        }
    }
}
